package datatypes;

public class datatypes {
    public static void main(String[] args) {
        double number1 = 12.5;
        System.out.println("Double tipinde değişken :" + number1);

        byte number2 = 127;
        System.out.println("Byte tipinde değişken : " + number2);

        String var1 = "ANKARA";
        System.out.println("String tipinde değişken :" + var1);

        char var2 = 'D';
        System.out.println("Char tipinde değişken : " + var2);
    }
}
